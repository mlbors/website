/*****************************/
/********** IMPORTS **********/
/*****************************/

import { browser, by, element } from 'protractor';

/********************************************************************************/
/********************************************************************************/

/************************************/
/********** APP PAGE CLASS **********/
/************************************/

export class AppPage {

  /*********************************/
  /********** NAVIGATE TO **********/
  /*********************************/

  navigateTo() {
    return browser.get('/about');
  }

  /********************************************************************************/
  /********************************************************************************/

  /************************************/
  /********** GET NAVIGATION **********/
  /************************************/

  getNavigation() {
    return element(by.css('.navbar'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*******************************/
  /********** GET BRAND **********/
  /*******************************/

  getBrand() {
    return element(by.css('.navbar-brand'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /********************************/
  /********** GET NAVBAR **********/
  /********************************/

  getNavigationList() {
    return element(by.css('.navbar-nav'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /******************************************/
  /********** GET NAVIGATION ITEMS **********/
  /******************************************/

  getNavigationItems() {
    return element.all(by.css('.nav-item'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*********************************/
  /********** GET CONTENT **********/
  /*********************************/

  getContent() {
    return element(by.css('.content'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /****************************/
  /********** GET H1 **********/
  /****************************/

  getH1() {
    return element(by.css('h1'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*************************************/
  /********** GET COVER IMAGE **********/
  /*************************************/

  getPostCoverImage() {
    return element(by.css('.post-cover-image'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*********************************/
  /********** GET EXCERPT **********/
  /*********************************/

  getExcerpt() {
    return element(by.css('.excerpt'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /**********************************/
  /********** GET SECTIONS **********/
  /**********************************/

  getSections() {
    return element(by.css('.content-section'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /****************************************/
  /********** GET SECTION IMAGES **********/
  /****************************************/

  getSectionImages() {
    return element.all(by.css('.content-section .image-section'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /******************************************/
  /********** GET SECTION HEADINGS **********/
  /******************************************/

  getSectionHeadings() {
    return element.all(by.css('.content-section h2'));
  }

}
