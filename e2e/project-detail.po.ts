/*****************************/
/********** IMPORTS **********/
/*****************************/

import { browser, by, element } from 'protractor';

/********************************************************************************/
/********************************************************************************/

/************************************/
/********** APP PAGE CLASS **********/
/************************************/

export class AppPage {

  /*********************************/
  /********** NAVIGATE TO **********/
  /*********************************/

  navigateTo() {
    return browser.get('/project/frank-the-sceptical-worm-from-outer-space');
  }

  /********************************************************************************/
  /********************************************************************************/

  /************************************/
  /********** GET NAVIGATION **********/
  /************************************/

  getNavigation() {
    return element(by.css('.navbar'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*******************************/
  /********** GET BRAND **********/
  /*******************************/

  getBrand() {
    return element(by.css('.navbar-brand'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /********************************/
  /********** GET NAVBAR **********/
  /********************************/

  getNavigationList() {
    return element(by.css('.navbar-nav'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /******************************************/
  /********** GET NAVIGATION ITEMS **********/
  /******************************************/

  getNavigationItems() {
    return element.all(by.css('.nav-item'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*********************************/
  /********** GET CONTENT **********/
  /*********************************/

  getContent() {
    return element(by.css('.content'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /****************************/
  /********** GET H1 **********/
  /****************************/

  getH1() {
    return element(by.css('h1'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*************************************/
  /********** GET COVER IMAGE **********/
  /*************************************/

  getProjectCoverImage() {
    return element(by.css('.project-cover-image'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /*****************************************/
  /********** GET CONTENT SECTION **********/
  /*****************************************/

  getContentSection() {
    return element(by.css('.content-section'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /****************************************/
  /********** GET IMAGES SECTION **********/
  /****************************************/

  getImagesSection() {
    return element(by.css('.images-section'));
  }

  /********************************************************************************/
  /********************************************************************************/

  /**************************************/
  /********** GET META SECTION **********/
  /**************************************/

  getMetaSection() {
    return element(by.css('.meta-section'));
  }

}
