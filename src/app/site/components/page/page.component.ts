import { Component, OnInit } from '@angular/core';
import { Router, ActivatedRoute } from '@angular/router';

import { PostComponent } from '../post/post.component';

/********************************************************************************/
/********************************************************************************/

/*******************************/
/********** COMPONENT **********/
/*******************************/

@Component({
  selector: 'app-page',
  templateUrl: './page.component.html',
  styleUrls: ['./page.component.scss']
})

/********************************************************************************/
/********************************************************************************/

/************************************/
/********** PAGE COMPONENT **********/
/************************************/

export class PageComponent implements OnInit {

  /*******************************/
  /********** ATTRIBUTS **********/
  /*******************************/

  /**
   * @var String slug page slug
   */

  public slug: String;

  /********************************************************************************/
  /********************************************************************************/

  /*********************************/
  /********** CONSTRUCTOR **********/
  /*********************************/

  /**
   * @param ActivatedRoute activateRoute information about associated route
   */

  constructor(private activatedRoute: ActivatedRoute) {
    this.activatedRoute.paramMap.subscribe(params => {
      const slug = params.get('slug');
      this._setValues(slug);
    });
  }

  /********************************************************************************/
  /********************************************************************************/

  /********************************/
  /********** NG ON INIT **********/
  /********************************/

  ngOnInit() {
  }

  /********************************************************************************/
  /********************************************************************************/

  /********************************/
  /********** SET VALUES **********/
  /********************************/

  /**
   * @param String slug page slug
   */

  private _setValues(slug: string) {
    this._setSlug(slug);
  }

  /********************************************************************************/
  /********************************************************************************/

  /******************************/
  /********** SET SLUG **********/
  /******************************/

  /**
   * @param String slug page slug
   */

  private _setSlug(slug: string) {
    if (typeof slug !== undefined && slug && slug.length > 0 && slug !== '') {
      this.slug = slug;
      return;
    }
    this.slug = 'home';
    return;
  }

}
